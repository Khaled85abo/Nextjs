export async function organizeByYear(data) {
  const organized = {};
  for (let order of data) {
    const datum = new Date(order.OrdDatum);
    const year = datum.getFullYear();
    const month = months[datum.getMonth()];

    if (!organized[year]) {
      organized[year] = {};
      organized[year][month] = [order];
    } else {
      if (!organized[year][month]) {
        organized[year][month] = [order];
      } else {
        organized[year][month].push(order);
      }
    }
  }
  return organized;
}

export async function getMonthlyValues(organized) {
  const totalPerMonth = [];

  for (let yearKey of Object.keys(organized)) {
    const aYear = organized[yearKey];
    for (let key of Object.keys(aYear)) {
      const aMonth = aYear[key];
      let total = 0;
      for (let order of aMonth) {
        total += order.OhOrdSumInklMoms;
      }
      // const total = aMonth.reduce((prev, curr)=> prev.OhOrdSumInklMoms + curr.OhOrdSumInklMoms, 0)
      const roundedTotal = Math.ceil(total);
      const obj = { month: `${key}`, total: roundedTotal, year: `${yearKey}` };
      totalPerMonth.push(obj);
    }
  }
  const sorted = totalPerMonth.sort(function (order1, order2) {
    if (order1.year > order2.year) return -1;
    if (order1.year < order2.year) return 1;

    if (months.indexOf(order1.month) > months.indexOf(order2.month)) return -1;
    if (months.indexOf(order2.month) < months.indexOf(order2.month)) return 1;
  });
  return sorted;
}

const getWholeYearSum = (organized, year = new Date().getFullYear()) => {
  let total = 0;
  if (organized[year]) {
    for (let [month, orders] of Object.entries(organized[year])) {
      let tot = 0;
      for (let order of orders) {
        tot += order.OhOrdSumInklMoms;
      }
      total += tot;
    }
  }
  return Math.ceil(total);
};

